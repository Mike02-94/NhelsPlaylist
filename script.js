// MAIN RENDER FUNCTION
// ===========================
function renderSongs(songsToRender = songs) {
    const playlist = document.getElementById('playlist');
    const emptyState = document.getElementById('emptyState');
    
    // Clear existing content
    playlist.innerHTML = '';
    
    // Show empty state if no songs
    if (songsToRender.length === 0) {
        emptyState.style.display = 'block';
        return;
    }
    
    emptyState.style.display = 'none';
    
    // Create a card for each song
    songsToRender.forEach(song => {
        const card = document.createElement('div');
        card.className = 'song-card';
        card.innerHTML = `
            <div class="song-header">
                <div class="song-title">${song.title}</div>
                <div class="song-info">${song.artist}</div>
                <button class="maximize-btn" onclick="maximizeCard(${song.id})" title="Expand">
                    <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
                        <path d="M15 3h6v6M9 21H3v-6M21 3l-7 7M3 21l7-7"/>
                    </svg>
                </button>
            </div>
            <div class="media-sections">
                <div class="media-section">
                    <div class="media-label">Audio Version</div>
                    <audio controls preload="metadata" data-song-id="${song.id}">
                        <source src="${song.audioSrc}" type="audio/mpeg">
                        Your browser does not support the audio element.
                    </audio>
                </div>
                <div class="media-section">
                    <div class="media-label">Video Performance</div>
                    <video controls preload="metadata">
                        <source src="${song.videoSrc}" type="video/mp4">
                        Your browser does not support the video element.
                    </video>
                </div>
            </div>
            <div class="song-footer">
                <button class="like-btn ${favorites.includes(song.id) ? 'liked' : ''}" 
                        onclick="toggleFavorite(${song.id})" 
                        title="Like">
                    ${favorites.includes(song.id) ? '❤️' : '🤍'}
                </button>
                <button class="notes-toggle" onclick="toggleNotes(${song.id})">
                    Lyrics
                </button>
            </div>
            <div class="notes-section ${openNotesId === song.id ? 'active' : ''}" id="notes-${song.id}">
                <pre class="notes-content">${song.notes}</pre>
            </div>
        `;
        playlist.appendChild(card);
    });

    // Add audio event listeners for auto-play functionality
    document.querySelectorAll('audio').forEach(audio => {
        audio.addEventListener('play', function() {
            handleAudioPlay(this);
        });

        audio.addEventListener('ended', function() {
            playNextSong(this);
        });
    });
}

