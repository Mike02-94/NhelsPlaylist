// MAXIMIZE CARD FEATURE
// ===========================
function maximizeCard(songId) {
    const song = songs.find(s => s.id === songId);
    if (!song) return;
    
    // Create modal overlay
    const modal = document.createElement('div');
    modal.className = 'modal-overlay';
    modal.innerHTML = `
        <div class="modal-content">
            <div class="modal-header">
                <div>
                    <div class="modal-title">${song.title}</div>
                    <div class="modal-artist">${song.artist}</div>
                </div>
                <button class="close-btn" onclick="closeModal()">
                    <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
                        <path d="M18 6L6 18M6 6l12 12"/>
                    </svg>
                </button>
            </div>
            
            <div class="modal-body">
                <div class="modal-media">
                    <div class="media-section">
                        <div class="media-label">Audio Version</div>
                        <audio controls preload="metadata" id="modal-audio">
                            <source src="${song.audioSrc}" type="audio/mpeg">
                            Your browser does not support the audio element.
                        </audio>
                    </div>
                    <div class="media-section">
                        <div class="media-label">Video Performance</div>
                        <video controls preload="metadata" id="modal-video">
                            <source src="${song.videoSrc}" type="video/mp4">
                            Your browser does not support the video element.
                        </video>
                    </div>
                    <div class="modal-actions">
                        <button class="like-btn ${favorites.includes(song.id) ? 'liked' : ''}" 
                                onclick="toggleFavoriteInModal(${song.id})" 
                                title="Like">
                            ${favorites.includes(song.id) ? '❤️' : '🤍'}
                        </button>
                    </div>
                </div>
                
                <div class="modal-lyrics">
                    <div class="lyrics-header">Lyrics</div>
                    <div class="lyrics-content">
                        <pre>${song.notes}</pre>
                    </div>
                </div>
            </div>
        </div>
    `;
    
    document.body.appendChild(modal);
    
    // Prevent body scroll when modal is open
    document.body.style.overflow = 'hidden';
    
    // Add event listener for modal audio/video
    const modalAudio = document.getElementById('modal-audio');
    const modalVideo = document.getElementById('modal-video');
    
    modalAudio.addEventListener('play', function() {
        if (!modalVideo.paused) modalVideo.pause();
    });
    
    modalVideo.addEventListener('play', function() {
        if (!modalAudio.paused) modalAudio.pause();
    });
    
    // Close modal when clicking outside
    modal.addEventListener('click', function(e) {
        if (e.target === modal) {
            closeModal();
        }
    });
}

function closeModal() {
    const modal = document.querySelector('.modal-overlay');
    if (modal) {
        // Pause any playing media
        modal.querySelectorAll('audio, video').forEach(media => media.pause());
        modal.remove();
        document.body.style.overflow = '';
    }
}

function toggleFavoriteInModal(songId) {
    toggleFavorite(songId);
    // Update the heart in modal
    const modalLikeBtn = document.querySelector('.modal-overlay .like-btn');
    if (modalLikeBtn) {
        if (favorites.includes(songId)) {
            modalLikeBtn.classList.add('liked');
            modalLikeBtn.innerHTML = '❤️';
        } else {
            modalLikeBtn.classList.remove('liked');
            modalLikeBtn.innerHTML = '🤍';
        }
    }
}

// ===========================
// AUDIO PLAYBACK CONTROL
// ===========================
function handleAudioPlay(audioElement) {
    // Pause all other audio and video elements when one plays
    document.querySelectorAll('audio, video').forEach(media => {
        if (media !== audioElement && !media.paused) {
            media.pause();
        }
    });
    currentAudio = audioElement;
}

// ===========================
// AUTO-PLAY NEXT SONG
// ===========================
function playNextSong(audioElement) {
    const songId = parseInt(audioElement.dataset.songId);
    const currentIndex = songs.findIndex(s => s.id === songId);
    
    // If not the last song, play the next one
    if (currentIndex < songs.length - 1) {
        const nextSong = songs[currentIndex + 1];
        const nextAudio = document.querySelector(`audio[data-song-id="${nextSong.id}"]`);
        if (nextAudio) {
            nextAudio.play();
            // Scroll to the next song card smoothly
            nextAudio.closest('.song-card').scrollIntoView({ 
                behavior: 'smooth', 
                block: 'center' 
            });
        }
    }
}

// ===========================
// FAVORITE/LIKE FUNCTIONALITY
// ===========================
function toggleFavorite(songId) {
    const index = favorites.indexOf(songId);
    
    if (index > -1) {
        // Remove from favorites
        favorites.splice(index, 1);
    } else {
        // Add to favorites
        favorites.push(songId);
    }
    
    // Save to localStorage so favorites persist
    localStorage.setItem('favorites', JSON.stringify(favorites));
    
    // Update heart icons directly without re-rendering
    updateHeartIcons(songId);
}

// ===========================
// UPDATE HEART ICONS WITHOUT RE-RENDERING
// ===========================
function updateHeartIcons(songId) {
    const isLiked = favorites.includes(songId);
    
    // Find all heart buttons for this song (in cards)
    document.querySelectorAll(`.like-btn[onclick="toggleFavorite(${songId})"]`).forEach(btn => {
        if (isLiked) {
            btn.classList.add('liked');
            btn.innerHTML = '❤️';
        } else {
            btn.classList.remove('liked');
            btn.innerHTML = '🤍';
        }
    });
}

// ===========================
// NOTES TOGGLE - NO REFRESH VERSION
// ===========================
function toggleNotes(songId) {
    const clickedSection = document.getElementById(`notes-${songId}`);
    
    // Close all other notes sections
    document.querySelectorAll('.notes-section').forEach(section => {
        if (section.id !== `notes-${songId}` && section.classList.contains('active')) {
            section.classList.remove('active');
        }
    });
    
    // Toggle the clicked one
    clickedSection.classList.toggle('active');
    
    // Update the global state tracker
    openNotesId = clickedSection.classList.contains('active') ? songId : null;
    
    // Scroll to the card if notes just opened
    if (clickedSection.classList.contains('active')) {
        setTimeout(() => {
            clickedSection.closest('.song-card').scrollIntoView({ 
                behavior: 'smooth', 
                block: 'nearest' 
            });
        }, 100);
    }
}

